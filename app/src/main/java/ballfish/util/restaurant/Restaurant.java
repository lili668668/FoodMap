public class Restaurant {
    public String name;
    public String detail;
    public String address;
    public String photos;
    public String _class;

    public Restaurant(String name, String detail, String address, String photos, String _class) {
        this.name = name;
        this.detail = detail;
        this.address = address;
        this.photos = photos;
        this._class = _class;
    }
}
